    private void jBSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSalvarActionPerformed
        // TODO add your handling code here:
        //verificando se o ususario informou os dados
        if(!jTFNome.getText().equals("")){
            if(!jTFCpf.getText().equals("")){
                if(jCBSexo.getSelectedIndex() != 0){
                    if(!jTFEmail.getText().equals("")){
                        
                        String nome = jTFNome.getText();
                        String cpf = jTFCpf.getText();
                        int sexo = jCBSexo.getSelectedIndex();
                        String mail = jTFEmail.getText();
                        
                        //crienado o objeto 
                        Cliente meuCliente = new Cliente(nome, cpf, sexo, mail);
                        
                        // adicionando na lista
                        this.listaCliente.add(meuCliente);
                        
                        JOptionPane.showMessageDialog(rootPane, "Cliente cadrastado com sucesso");
                        limpaCampus();
                        
                    }else{
                        JOptionPane.showMessageDialog(rootPane, "Erro: informe o email");
                    }
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Erro: informe o sexo");
                }
            }else{
                JOptionPane.showMessageDialog(rootPane, "Erro: informe o CPF");
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "Erro: informe o nome");
        }
    }//GEN-LAST:event_jBSalvarActionPerformed
    
    public void limpaCampus(){
        jTFNome.setText("");
        jTFCpf.setText("");
        jCBSexo.setSelectedIndex(0);
        jTFEmail.setText("");
    }
  
