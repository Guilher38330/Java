    public void mostraContaBancaria(){
         ContaBancaria minhaConta =this.listaContaBancaria.get(jTContas.getSelectedRow());
            JOptionPane.showMessageDialog(rootPane, "Nome: "+minhaConta.getNomeTitular()+ ", CPF: "+minhaConta.getCpfTitular()+ ", Saldo: "+minhaConta.getSaldo());
           
            //verificando se tratace de uma das sub classes 
            if(minhaConta instanceof ContaEspecial){
                ContaEspecial minhaContaEspecial = (ContaEspecial) minhaConta;
                JOptionPane.showMessageDialog(rootPane, "Limite: "+minhaContaEspecial.getLimite());
            }else if(minhaConta instanceof ContaPoupanca){
                ContaPoupanca minhaContaPoupanca = (ContaPoupanca) minhaConta;
                JOptionPane.showMessageDialog(rootPane, "Dia de Rendimento: "+minhaContaPoupanca.getDiaRendimento());
            }
}


