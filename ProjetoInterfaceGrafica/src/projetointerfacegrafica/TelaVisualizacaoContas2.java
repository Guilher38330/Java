    //metodo responsavel por carregar a tabela
    public void atualizaTabela(){
        DefaultTableModel modelo = (DefaultTableModel)jTContas.getModel();
        modelo.setNumRows(0);
        
        for(int i = 0; i < this.listaContaBancaria.size(); i++){
            ContaBancaria minhaConta = this.listaContaBancaria.get(i);
            modelo.addRow(new Object[] {""});
            modelo.setValueAt(minhaConta.getNomeTitular(), i, 0);
            modelo.setValueAt(minhaConta.getCpfTitular(), i, 1);
            modelo.setValueAt(minhaConta.getSaldo(), i, 2);
            
            String tipo = "";
            if(minhaConta instanceof ContaEspecial){
                tipo = "Especial";
                //modelo.setValueAt("Especial", i, 3);
            }else if(minhaConta instanceof ContaPoupanca){
                tipo = "Poupanca";
                //modelo.setValueAt("Poupanca", i, 3);
            }else{
                tipo = "Bancaria";
            }
            modelo.setValueAt(tipo, i, 3);
        }
    }

